let age = 20;
let hasID = true;

// Relational operators
console.log(age >= 18);   // true
console.log(age < 18);    // false

// Logical operators
console.log(age >= 18 && hasID); // true (both true)
console.log(age >= 18 || hasID); // true (at least one true)
console.log(!(age >= 18));       // false (NOT operator)